// Function to access a Glyph by its value (character)
pub fn get_glyph(font: *Font, value: u8) ?*const Glyph {
    var i: usize = 0;
    while (i < font.glyphs.len) {
        if (font.glyphs[i].value == value) {
            return &font.glyphs[i];
        }
        i += 1;
    }
    return null; // Return null if the glyph is not found
}
